import os
import sys
import json
import time
import requests
import logging
import subprocess
from pathlib import Path
from datetime import datetime
from dotenv import load_dotenv

# Configurar logging detalhado
logging.basicConfig(
    level=logging.DEBUG,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(),
        logging.FileHandler('webhook_test.log')
    ]
)
logger = logging.getLogger('webhook_test')

def load_env_vars():
    """Carrega vari√°veis de ambiente do arquivo .env."""
    # Encontrar o diret√≥rio raiz do projeto
    current_dir = Path(__file__).resolve().parent
    root_dir = current_dir.parent.parent  # Subir dois n√≠veis: webhook/scripts -> projeto raiz
    
    # Caminho para o arquivo .env
    env_path = root_dir / '.env'
    
    if env_path.exists():
        logger.info(f"üìÅ Carregando vari√°veis de ambiente de {env_path}")
        load_dotenv(dotenv_path=env_path)
        return True
    else:
        logger.error(f"‚ùå Arquivo .env n√£o encontrado em {env_path}")
        return False

def get_ngrok_url():
    """Obt√©m a URL p√∫blica do ngrok consultando sua API local."""
    try:
        logger.info("üîç Verificando URL do Ngrok...")
        response = requests.get("http://localhost:4040/api/tunnels")
        tunnels = response.json()["tunnels"]
        for tunnel in tunnels:
            if tunnel["proto"] == "https":
                logger.info(f"‚úÖ URL do Ngrok encontrada: {tunnel['public_url']}")
                return tunnel["public_url"]
    except Exception as e:
        logger.error(f"‚ùå Erro ao obter URL do ngrok: {str(e)}")
    return None

def check_webhook_server():
    """Verifica se o servidor webhook est√° rodando."""
    logger.info("üîç Verificando se o servidor webhook est√° rodando...")
    
    # Obter a porta do webhook do arquivo .env
    webhook_port = os.environ.get("WEBHOOK_PORT", "8001")
    webhook_host = os.environ.get("WEBHOOK_HOST", "0.0.0.0")
    
    try:
        # Verificar se a porta est√° em uso
        result = subprocess.run(
            f"lsof -i:{webhook_port}", 
            shell=True, 
            capture_output=True, 
            text=True
        )
        
        if result.returncode == 0:
            logger.info(f"‚úÖ Servidor webhook rodando na porta {webhook_port}")
            return True
        else:
            logger.warning(f"‚ö†Ô∏è Servidor webhook n√£o encontrado na porta {webhook_port}")
            
            # Verificar processos Python em execu√ß√£o
            python_processes = subprocess.run(
                "ps aux | grep python | grep -v grep", 
                shell=True, 
                capture_output=True, 
                text=True
            )
            
            logger.info(f"üìã Processos Python em execu√ß√£o:\n{python_processes.stdout}")
            return False
    except Exception as e:
        logger.error(f"‚ùå Erro ao verificar servidor webhook: {str(e)}")
        return False

def test_webhook_endpoint(webhook_url):
    """Testa o endpoint do webhook enviando uma mensagem de teste."""
    logger.info(f"üîÑ Testando endpoint do webhook: {webhook_url}")
    
    # Criar uma mensagem de teste similar ao formato do Chatwoot
    test_message = {
        "event": "message_created",
        "account": {
            "id": 1
        },
        "message": {
            "id": 123456,
            "content": "Mensagem de teste do script de verifica√ß√£o",
            "message_type": 0,
            "content_type": "text"
        },
        "conversation": {
            "id": 789012,
            "inbox_id": 345678
        },
        "contact": {
            "id": 901234,
            "name": "Usu√°rio de Teste"
        }
    }
    
    try:
        # Adicionar cabe√ßalho de autentica√ß√£o se configurado
        headers = {}
        auth_token = os.environ.get("WEBHOOK_AUTH_TOKEN")
        if auth_token:
            logger.info("üîê Adicionando token de autentica√ß√£o ao cabe√ßalho")
            headers["X-Auth-Token"] = auth_token
        
        # Enviar requisi√ß√£o POST para o webhook
        logger.info(f"üì§ Enviando mensagem de teste para: {webhook_url}")
        logger.debug(f"üìã Conte√∫do da mensagem: {json.dumps(test_message, indent=2)}")
        
        response = requests.post(
            webhook_url,
            json=test_message,
            headers=headers,
            timeout=10
        )
        
        # Verificar resposta
        logger.info(f"üì• Resposta recebida: C√≥digo {response.status_code}")
        logger.debug(f"üìã Conte√∫do da resposta: {response.text}")
        
        if response.status_code == 200:
            logger.info("‚úÖ Teste do webhook bem-sucedido!")
            return True
        else:
            logger.error(f"‚ùå Falha no teste do webhook: C√≥digo {response.status_code}")
            return False
    except Exception as e:
        logger.error(f"‚ùå Erro ao testar webhook: {str(e)}")
        return False

def check_vps_connection():
    """Verifica a conex√£o com a VPS."""
    logger.info("üîç Verificando conex√£o com a VPS...")
    
    # Obter credenciais da VPS
    vps_host = os.environ.get("VPS_HOST")
    
    if not vps_host:
        logger.error("‚ùå Host da VPS n√£o encontrado no arquivo .env")
        return False
    
    try:
        # Tentar fazer um ping na VPS
        logger.info(f"üîÑ Testando conex√£o com {vps_host}...")
        
        ping_process = subprocess.run(
            f"ping -c 3 {vps_host}",
            shell=True,
            capture_output=True,
            text=True
        )
        
        if ping_process.returncode == 0:
            logger.info(f"‚úÖ Conex√£o com a VPS estabelecida: {vps_host}")
            logger.debug(f"üìã Resultado do ping:\n{ping_process.stdout}")
            return True
        else:
            logger.error(f"‚ùå Falha ao conectar com a VPS: {vps_host}")
            logger.debug(f"üìã Erro do ping:\n{ping_process.stderr}")
            return False
    except Exception as e:
        logger.error(f"‚ùå Erro ao verificar conex√£o com a VPS: {str(e)}")
        return False

